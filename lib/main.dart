      ),
      home: const HomePage(),
    );
  }
}

class HomePage extends StatelessWidget {
  const HomePage({super.key});

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: const Text('Weather App')),
      body: const Center(
        child: Text(
          'Welcome to Weather App',
          style: TextStyle(fontSize: 24),
        ),
      ),
    );
  }
}
